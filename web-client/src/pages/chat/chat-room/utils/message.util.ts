export function formatChatText(text: string): string {
  // Convert URLs to clickable links
  const urlRegex =
    /(\b(https?|ftp|file):\/\/[-A-Z0-9+&@#/%?=~_|!:,.;]*[-A-Z0-9+&@#/%=~_|]|www\.[a-zA-Z0-9-]+(\.[a-zA-Z]{2,})+)/gi;
  const linkedText = text.replace(urlRegex, (url) => {
    let href = url;
    if (!/^https?:\/\//i.test(url)) {
      href = `http://${url}`;
    }
    return `<a href="${href}" target="_blank" rel="noopener noreferrer">${url}</a>`;
  });

  const emojiFormattedText = linkedText.replace(
    /:\)|:\(|:D|:P|<3|XD|:o|:thumbsup:|:thumbsdown:|:fire:|:star:|:rocket:/g,
    (match) => {
      return emojiMap[match] || match;
    },
  );

  // Convert newlines to <br> tags
  const newLineFormattedText = emojiFormattedText.replace(/\n/g, '<br>');

  // Highlight mentions
  const mentionRegex = /@([a-zA-Z0-9_]+)/g;
  const mentionFormattedText = newLineFormattedText.replace(
    mentionRegex,
    '<span class="mention">@$1</span>',
  );

  // emoji regex
  const emojiRegex = /^(?:[\p{Emoji}\u200d\ufe0f]+)$/u;
  const emojiText = mentionFormattedText.replace(emojiRegex, (match) => {
    return `<span class="chat-message-emoji">${match}</span>`;
  });

  // Convert hashtags to links
  const hashtagRegex = /#(\w+)/g;
  const hashtagFormattedText = emojiText.replace(
    hashtagRegex,
    '<a href="/hashtag/$1" class="hashtag">#$1</a>',
  );

  return hashtagFormattedText;
}
